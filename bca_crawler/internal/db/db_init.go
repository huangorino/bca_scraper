package db

import (
	"bca_crawler/internal/utils"
	"fmt"

	"github.com/jmoiron/sqlx"
)

const schema_pg = `
CREATE TABLE IF NOT EXISTS announcements (
	id SERIAL PRIMARY KEY,
	ann_id INTEGER UNIQUE,
	title TEXT,
	link TEXT UNIQUE,
	company_name TEXT,
	stock_name TEXT,
	date_posted TIMESTAMP,
	category TEXT,
	ref_number TEXT,
	content TEXT,
	attachments TEXT
);
CREATE INDEX IF NOT EXISTS idx_ann_date_posted ON announcements(date_posted);


CREATE TABLE IF NOT EXISTS stocks (
  id SERIAL PRIMARY KEY,
  stock_code VARCHAR(50) UNIQUE,
  stock_name VARCHAR(255),
  company_name VARCHAR(255),
  market VARCHAR(100),
  sector VARCHAR(100),
  website VARCHAR(255),
  status VARCHAR(10) DEFAULT 'ACTIVE'
);


CREATE TABLE IF NOT EXISTS daily_stock_prices (
  id SERIAL PRIMARY KEY,
  stock_code VARCHAR(50) NOT NULL,
  date DATE NOT NULL,
  close_price DECIMAL(10,4),
  change DECIMAL(10,4),
  change_perc DECIMAL(5,2),
  volume BIGINT,
  recommend varchar(30),
  high_price DECIMAL(10,4),
  low_price DECIMAL(10,4),
  buy_qty BIGINT,
  sell_qty BIGINT,
  mkt_cap DECIMAL(20,4),
  pe DECIMAL(10,4),
  eps DECIMAL(10,4),
  esg_rating INTEGER,
  UNIQUE(stock_code, date)
);
CREATE INDEX IF NOT EXISTS idx_daily_stock_prices_stock_code ON daily_stock_prices(stock_code);
CREATE INDEX IF NOT EXISTS idx_daily_stock_prices_date ON daily_stock_prices(date);


CREATE TABLE IF NOT EXISTS entities ( 
    id SERIAL PRIMARY KEY,
    primary_perm_id INTEGER,
    secondary_perm_id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 100001 INCREMENT BY 1),
    display_name TEXT NOT NULL,
    name TEXT,
    salutation TEXT,
    stock_code TEXT,
    birth_year INTEGER,
    gender TEXT,
    nationality TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE IF NOT EXISTS boardroom_changes (
    id SERIAL PRIMARY KEY,
    ann_id INTEGER UNIQUE,
    company_name TEXT,
    stock_code TEXT,
    person_name TEXT,
    person_title TEXT,
    person_birth_year INTEGER,
    person_gender TEXT,
    person_nationality TEXT,
    date_announced DATE,
    date_of_change DATE,
    designation TEXT,
    previous_position TEXT,
    remarks TEXT,
    directorate TEXT,
    type_of_change TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE IF NOT EXISTS backgrounds (
    id SERIAL PRIMARY KEY,
    perm_id INTEGER NOT NULL,
    qualification TEXT,
    working_experience TEXT,
    directorships TEXT,
    family_relationship TEXT,
    conflict_of_interest TEXT,
    interest_in_securities TEXT
);
CREATE UNIQUE INDEX IF NOT EXISTS idx_backgrounds_perm_id ON backgrounds(perm_id);

`

// DriverType represents supported database drivers
type DriverType string

const (
	DriverPostgres DriverType = "postgres"
)

// Setup initializes and verifies the database schema
func Setup(connStr string, driver DriverType) (*sqlx.DB, error) {
	var db *sqlx.DB
	var err error

	switch driver {
	case DriverPostgres:
		db, err = sqlx.Open("postgres", connStr+"?sslmode=disable")
		if err != nil {
			return nil, err
		}

		if _, err := db.Exec(schema_pg); err != nil {
			db.Close()
			return nil, err
		}
	}

	utils.Logger.Infof("Database initialized and schema verified (%s)", connStr)
	return db, nil
}

// Connect connects to an existing PostgreSQL database without altering schema
func Connect(connStr string, driver DriverType) (*sqlx.DB, error) {
	var db *sqlx.DB
	var err error

	switch driver {
	case DriverPostgres:
		db, err = sqlx.Open("postgres", connStr+"?sslmode=disable")
	}

	if err != nil {
		return nil, fmt.Errorf("open db: %w", err)
	}
	if err := db.Ping(); err != nil {
		return nil, fmt.Errorf("ping db: %w", err)
	}
	utils.Logger.Infof("Connected to database: %s", connStr)
	return db, nil
}
